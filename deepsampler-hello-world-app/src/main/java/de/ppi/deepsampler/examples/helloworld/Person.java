package de.ppi.deepsampler.examples.helloworld;

/**
 * A simple Bean, that describes a Person.
 */
public class Person {

    private int id;
    private String name;

    public Person(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
